package Exercicios5;

public class Cars {

    private String model;
    private String color;
    private int year;

    public String getColor() {
        return color;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public Cars (String model, String color, int year){

        this.model = model;
        this.color = color;
        this.year = year;



    }
}
